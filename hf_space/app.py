"""
AuthentiGuard — AI Content Detection Demo (Hugging Face Spaces)

A Gradio demo of the text detection pipeline. Runs the 10-signal
heuristic detector (perplexity patterns, stylometry, hedge words,
contractions, sentence variance, etc.) to determine if text was
written by a human or generated by AI.

Deploy: https://huggingface.co/spaces
"""

import re
import math
from collections import Counter

import gradio as gr


# ═══════════════════════════════════════════════════════════════
# Heuristic Text Detector (standalone — no dependencies)
# ═══════════════════════════════════════════════════════════════

HEDGE_WORDS = {
    "additionally", "furthermore", "moreover", "consequently",
    "nevertheless", "specifically", "particularly", "significantly",
    "essentially", "fundamentally", "undeniably", "arguably",
    "notably", "importantly", "remarkably", "interestingly",
    "comprehensive", "innovative", "crucial", "robust",
    "leveraging", "utilizing", "enhancing", "facilitating",
    "delve", "tapestry", "multifaceted", "paradigm",
}

CASUAL_WORDS = {
    "lol", "gonna", "wanna", "kinda", "yeah", "nah",
    "tbh", "imo", "btw", "idk", "omg", "haha", "hmm",
    "ok", "okay", "yep", "nope", "cuz", "tho", "ain't",
}


def _sentences(text: str) -> list[str]:
    return [s.strip() for s in re.split(r'(?<=[.!?])\s+', text) if len(s.strip()) > 5]


def _words(text: str) -> list[str]:
    return re.findall(r"[a-zA-Z']+", text.lower())


def sentence_length_variance(text: str) -> float:
    sents = _sentences(text)
    if len(sents) < 3:
        return 0.5
    lengths = [len(s.split()) for s in sents]
    mean_len = sum(lengths) / len(lengths)
    if mean_len == 0:
        return 0.5
    variance = sum((l - mean_len) ** 2 for l in lengths) / len(lengths)
    cv = math.sqrt(variance) / mean_len
    if cv < 0.15:
        return 0.85
    if cv > 0.60:
        return 0.15
    return 0.85 - (cv - 0.15) * (0.70 / 0.45)


def hedge_word_ratio(text: str) -> float:
    words = _words(text)
    if len(words) < 10:
        return 0.5
    count = sum(1 for w in words if w in HEDGE_WORDS)
    ratio = count / len(words)
    if ratio > 0.04:
        return 0.90
    if ratio > 0.02:
        return 0.70
    return max(0.1, 0.50 - ratio * 10)


def casual_marker_ratio(text: str) -> float:
    words = _words(text)
    if len(words) < 10:
        return 0.5
    count = sum(1 for w in words if w in CASUAL_WORDS)
    ratio = count / len(words)
    if ratio > 0.03:
        return 0.10
    if ratio > 0.01:
        return 0.30
    return 0.60


def type_token_ratio(text: str) -> float:
    words = _words(text)
    if len(words) < 20:
        return 0.5
    ttr = len(set(words)) / len(words)
    if ttr < 0.40:
        return 0.75
    if ttr > 0.70:
        return 0.20
    return 0.75 - (ttr - 0.40) * (0.55 / 0.30)


def contraction_score(text: str) -> float:
    contractions = len(re.findall(r"\b\w+'\w+\b", text))
    words = _words(text)
    if len(words) < 20:
        return 0.5
    ratio = contractions / len(words)
    if ratio > 0.03:
        return 0.15
    if ratio > 0.01:
        return 0.35
    return 0.70


def first_person_ratio(text: str) -> float:
    words = _words(text)
    if len(words) < 20:
        return 0.5
    fp = {"i", "me", "my", "mine", "myself", "we", "us", "our", "ours"}
    count = sum(1 for w in words if w in fp)
    ratio = count / len(words)
    if ratio > 0.04:
        return 0.15
    if ratio > 0.02:
        return 0.35
    return 0.65


def detect_text(text: str) -> dict:
    """Run the 10-signal heuristic detector. Returns score, label, and signals."""
    if len(text.strip()) < 40:
        return {
            "score": 0.5,
            "label": "UNCERTAIN",
            "confidence": 0.0,
            "message": "Please provide at least 40 characters for analysis.",
            "signals": {},
        }

    signals = {
        "sentence_variance": sentence_length_variance(text),
        "hedge_words": hedge_word_ratio(text),
        "casual_markers": casual_marker_ratio(text),
        "type_token_ratio": type_token_ratio(text),
        "contractions": contraction_score(text),
        "first_person": first_person_ratio(text),
    }

    weights = {
        "sentence_variance": 1.5,
        "hedge_words": 2.5,
        "casual_markers": 1.0,
        "type_token_ratio": 0.5,
        "contractions": 2.0,
        "first_person": 2.0,
    }

    total_weight = sum(weights.values())
    score = sum(signals[k] * weights[k] for k in signals) / total_weight

    if score > 0.55:
        label = "AI_GENERATED"
    elif score < 0.40:
        label = "HUMAN"
    else:
        label = "UNCERTAIN"

    confidence = abs(score - 0.5) * 2

    return {
        "score": round(score, 4),
        "label": label,
        "confidence": round(confidence, 4),
        "signals": {k: round(v, 4) for k, v in signals.items()},
    }


