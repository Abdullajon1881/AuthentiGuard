def analyze(text: str) -> str:
    result = detect_text(text)
    score = result["score"]
    label = result["label"]
    conf = result["confidence"]

    label_display = {
        "AI_GENERATED": "AI Generated",
        "HUMAN": "Human Written",
        "UNCERTAIN": "Uncertain",
    }

    output = f"## Result: {label_display.get(label, label)}\n\n"
    output += f"**AI Score:** {score:.2f} (0 = human, 1 = AI)\n\n"
    output += f"**Confidence:** {conf:.0%}\n\n"
    output += "### Signal Breakdown\n\n"
    output += "| Signal | Score | Interpretation |\n"
    output += "|--------|-------|----------------|\n"

    signal_names = {
        "sentence_variance": "Sentence Length Variance",
        "hedge_words": "Hedge / Filler Words",
        "casual_markers": "Casual Language",
        "type_token_ratio": "Vocabulary Diversity",
        "contractions": "Contractions",
        "first_person": "First Person Pronouns",
    }

    for key, val in result["signals"].items():
        interp = "AI-like" if val > 0.6 else "Human-like" if val < 0.4 else "Neutral"
        output += f"| {signal_names.get(key, key)} | {val:.2f} | {interp} |\n"

    output += "\n---\n"
    output += "*This is a heuristic detector using statistical text features. "
    output += "For production use with ML-based detection, see the full [AuthentiGuard API](https://github.com/Abdullajon1881/AuthentiGuard).*"

    return output


demo = gr.Interface(
    fn=analyze,
    inputs=gr.Textbox(
        label="Paste text to analyze",
        placeholder="Enter at least 40 characters of text...",
        lines=8,
    ),
    outputs=gr.Markdown(label="Analysis Result"),
    title="AuthentiGuard — AI Content Detector",
    description=(
        "Detect whether text was written by a human or generated by AI. "
        "This demo uses a 6-signal heuristic ensemble analyzing sentence patterns, "
        "vocabulary, hedge words, contractions, and more. "
        "Part of the [AuthentiGuard](https://github.com/Abdullajon1881/AuthentiGuard) "
        "multi-modal AI detection platform."
    ),
    examples=EXAMPLES,
    theme=gr.themes.Base(),
    allow_flagging="never",
)

if __name__ == "__main__":
    demo.launch()
